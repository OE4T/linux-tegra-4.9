#define CBBNOC_BIT(_bit_) (1ULL << (_bit_))
#define CBBNOC_MASK(_msb_, _lsb_) \
	((CBBNOC_BIT(_msb_+1) - 1) & ~(CBBNOC_BIT(_lsb_) - 1))
#define CBBNOC_EXTRACT(_x_, _msb_, _lsb_)  \
	((_x_ & CBBNOC_MASK(_msb_, _lsb_)) >> _lsb_)


